                    <h2 class="section-heading">Our Theme</h2><br><br><hp></div>
<p style="font-size:120%;">Violence threatens peace on our precious Planet Earth. The ongoing war in Syria, terrorist acts around the world, abuse of women and children, mistreatment of minorities, hate towards those who are different and disrespect to animals and the environment are but a few examples of the manifestation of this violence. Violence has penetrated societies and communities, and we see little effort invested by governments to combat it. Violence is a disease that needs an urgent remedy to ensure a safer world for future generations.<br>
Without tackling violence at its root, our world cannot prosper. Places affected by violence are unable to thrive. Violence leads to a chain of adverse effects. Where there is violence, economies are hard hit, human rights are abused, gender inequality increases, access to education becomes limited and diseases spread out of control. The people hit worst by violence are the vulnerable: children, women and the elderly. <br>
This is why the GALMUN team for this year's conference held on March 20-21 has chosen the theme 'Combatting Violence and Promoting Peaceful Resolutions'. It is our hope that by focusing on this issue, we will be able to raise awareness in the communities we come from and promote peaceful management of conflicts. Delegates will tackle different aspects of violence in eight committees: Security Council, UN Women, ECOSOC, WHO, Human Rights, Disarmament, UNICEF and Environment. The topics discussed in each committee can be viewed on this website. <br>
The GALMUN team for 2017 is looking forward to this year's conference. We hope that both delegates and advisors will have a fruitful and enjoyable time in Nazareth, the heart of Galilee. <br></p>

                   
                
