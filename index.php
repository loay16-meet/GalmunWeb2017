Our third conference will be aimed at bringing students from different backgrounds together for a two-day debate. It will help them become global citizens, exposing them to significant issues both nationally and internationally. They will conduct research at a high level, improve their public speaking skills and develop their English beyond the classroom setting. <br>
The conference is designed for students from Grades 10-12 and we welcome new delegates as well as those with previous experience. Delegates will be required to prepare position papers and draft clauses that address the issues of their committees. Due to increased interest in our conference, this year we can only guarantee 8 delegates per school. However, we will keep a waiting list for any additional places that become available. A registration form is attached with this invitation, in addition to a letter providing detailed information about the conference. <br>
